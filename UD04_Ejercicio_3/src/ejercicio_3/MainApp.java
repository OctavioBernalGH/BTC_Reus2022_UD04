package ejercicio_3;

public class MainApp {

	public static void main(String[] args) {
		
		/* Asignación de variables */
		int X = 5;
		int Y = 10;
		double N = 15;
		double M = 20;
		
		/* Salida de datos */
		System.out.println("Valor X: " + X);
		System.out.println("Valor Y: " + Y);
		System.out.println("Valor N: " + N);
		System.out.println("Valor M: " + M);
		System.out.println("La suma de X e Y es: " + (X + Y));
		System.out.println("La diferencia entre X e Y es: " + (X - Y));
		System.out.println("El producto entre X e Y es: " + (X * Y));
		System.out.println("El cociente entre X e Y es: " + (X / Y));
		System.out.println("El resto entre X e Y es: " + (X % Y));
		System.out.println("La suma de N y M es: " + (N + M));
		System.out.println("La diferencia entre N y M es: " + (N - M));
		System.out.println("El producto entre N y M es: " + (N * M));
		System.out.println("El cociente entre N y M es: " + (N / M));
		System.out.println("El resto entre N y M es: " + (N % M));
		System.out.println("La suma entre X y N es: " + (X + N));
		System.out.println("El cociente entre Y y M es: " + (Y / M));
		System.out.println("El resto entre Y y M es: " + (Y % M));
		System.out.println("El doble de variable X es: " + X);
		System.out.println("El doble de variable Y es: " + Y);
		System.out.println("El doble de variable N es: " + N);
		System.out.println("El doble de variable M es: " + M);
		System.out.println("La suma de todas es: " + (X + Y + N + M));
		System.out.println("El producto de todas es: " + (X * Y * N * M));
		
		
		
	}

}
